package org.secondfloor;

public class EmployeeData {
	
	private int rollno;
	private int std;
	private String email;
	private long phone;
	private String name;
	
	public int getRollno() {
		return rollno;
	}
	public void setRollno(int r) {
		this.rollno = r;
	}
	public int getStd() {
		return std;
	}
	public void setStd(int s) {
		this.std = s;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String e) {
		this.email = e;
	}
	public long getPhone() {
		return phone;
	}
	public void setPhone(long p) {
		this.phone = p;
	}
	public String getName() {
		return name;
	}
	public void setName(String n) {
		this.name = n;
	}
	
}